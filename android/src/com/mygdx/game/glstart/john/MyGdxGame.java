    private boolean cekComponent(int x, int y) {
        // nilai 1, 2, 3, adalah obsLvl[] yang tidak bisa dilewati
        if (obsLVL1[x][y] == 1 || obsLVL1[x][y] == 2 || obsLVL1[x][y] == 3 || obsLVL1[x][y] == 4) {
            return true;
        } else {
            return false;
        }
    }

    private void openComponent(int x, int y) {
        // tuas
        if (obsLVL1[POSISIY][POSISIX] == 5 || obsLVL1[POSISIY][POSISIX] == 6) {
            switch (stateTuas) {
                case 0:
                    obsLVL1[x][y] = 0;
                    stateTuas = 1;
                    obsLVL1[POSISIY][POSISIX] = 6;
                    break;
                case 1:
                    obsLVL1[x][y] = 2;
                    stateTuas = 0;
                    obsLVL1[POSISIY][POSISIX] = 5;
                    break;
            }
        } // kunci
        else if (obsLVL1[POSISIY][POSISIX] == 9) {
            inventorisKunci++;
            obsLVL1[POSISIY][POSISIX] = 0;
        }
    }

    private void cekPintu(int x, int y) {
        // posisi pemain y - 1 == pintu
        if (obsLVL1[POSISIY + 1][POSISIX] == 7 || obsLVL1[POSISIY][POSISIX - 1] == 7) {
            // kunci tidak nol
            if (inventorisKunci != 0) {
                obsLVL1[x][y] = 8;
                mapLVL1[x][y] = 5;
                inventorisKunci--;
            }
        }
    }

